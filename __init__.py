## The Python interpreter recognizes a folder as the package if it contains __init__.py file.
## IncortaSpark becomes a package
## An empty __init__.py file makes all functions 
##     from the python code in this folder available when this package is imported.
## The __init__.py file is normally kept empty. However, it can also be used to 
##     choose specific functions from modules in the package folder and 
##     make them available for import.


